const { capitalize, toScreamingSnake, inferPropType } = require("./utils");

/**
 * Generate state management code for a set of props using a specific library.
 *
 * @param {object} options
 * @param {string} options.storeName - Name for the store (e.g. "user", "theme")
 * @param {string[]} options.props - Prop names to include in the store
 * @param {"redux"|"zustand"|"jotai"} options.library - Target library
 * @param {boolean} [options.typescript=true] - Generate TypeScript types
 * @returns {{ code: string, library: string, dependencies: string[] }}
 */
function generateStore(options) {
  const { storeName, props, library, typescript = true } = options;

  switch (library) {
    case "redux":
      return generateReduxStore(storeName, props, typescript);
    case "zustand":
      return generateZustandStore(storeName, props, typescript);
    case "jotai":
      return generateJotaiAtoms(storeName, props, typescript);
    default:
      throw new Error(`Unsupported library: ${library}. Supported: redux, zustand, jotai`);
  }
}

/**
 * Generate a Redux Toolkit slice + store configuration.
 */
function generateReduxStore(storeName, props, typescript) {
  const name = capitalize(storeName);
  const cleanProps = props.filter((p) => !p.startsWith("..."));

  const interfaceBlock = typescript
    ? `export interface ${name}State {\n${cleanProps.map((p) => `  ${p}: ${inferPropType(p)};`).join("\n")}\n}\n`
    : "";

  const initialState = cleanProps
    .map((p) => {
      const type = inferPropType(p);
      const defaultVal = getDefaultValue(type);
      return `  ${p}: ${defaultVal},`;
    })
    .join("\n");

  const reducers = cleanProps
    .map((p) => {
      const actionType = typescript ? `action: PayloadAction<${inferPropType(p)}>` : "action";
      return `    set${capitalize(p)}(state, ${actionType}) {\n      state.${p} = action.payload;\n    },`;
    })
    .join("\n");

  const selectors = cleanProps
    .map((p) => {
      const stateType = typescript ? `: { ${storeName}: ${name}State }` : "";
      return `export const select${capitalize(p)} = (state${stateType}) => state.${storeName}.${p};`;
    })
    .join("\n");

  const exportedActions = cleanProps
    .map((p) => `set${capitalize(p)}`)
    .join(", ");

  const code = `// Auto-generated by react-prop-optimizer — Redux Toolkit
import { createSlice${typescript ? ", PayloadAction" : ""} } from "@reduxjs/toolkit";

${interfaceBlock}const initialState${typescript ? `: ${name}State` : ""} = {
${initialState}
};

const ${storeName}Slice = createSlice({
  name: "${storeName}",
  initialState,
  reducers: {
${reducers}
  },
});

export const { ${exportedActions} } = ${storeName}Slice.actions;
export default ${storeName}Slice.reducer;

// Selectors
${selectors}

// --- Usage Example ---
// import { useSelector, useDispatch } from "react-redux";
// const value = useSelector(select${capitalize(cleanProps[0] || "value")});
// const dispatch = useDispatch();
// dispatch(set${capitalize(cleanProps[0] || "Value")}(newValue));
`;

  return {
    code,
    library: "redux",
    dependencies: ["@reduxjs/toolkit", "react-redux"],
  };
}

/**
 * Generate a Zustand store.
 */
function generateZustandStore(storeName, props, typescript) {
  const name = capitalize(storeName);
  const cleanProps = props.filter((p) => !p.startsWith("..."));

  const stateFields = cleanProps
    .map((p) => `  ${p}: ${inferPropType(p)};`)
    .join("\n");

  const actionFields = cleanProps
    .map((p) => `  set${capitalize(p)}: (value: ${inferPropType(p)}) => void;`)
    .join("\n");

  const initialValues = cleanProps
    .map((p) => {
      const type = inferPropType(p);
      return `  ${p}: ${getDefaultValue(type)},`;
    })
    .join("\n");

  const setters = cleanProps
    .map(
      (p) =>
        `  set${capitalize(p)}: (value) => set({ ${p}: value }),`
    )
    .join("\n");

  const interfaceBlock = typescript
    ? `export interface ${name}Store {\n${stateFields}\n${actionFields}\n}\n\n`
    : "";

  const typeAnnotation = typescript ? `<${name}Store>` : "";

  const code = `// Auto-generated by react-prop-optimizer — Zustand
import { create } from "zustand";

${interfaceBlock}export const use${name}Store = create${typeAnnotation}((set) => ({
${initialValues}
${setters}
}));

// --- Usage Example ---
// import { use${name}Store } from "./${storeName}Store";
//
// function MyComponent() {
//   const { ${cleanProps[0] || "value"}, set${capitalize(cleanProps[0] || "value")} } = use${name}Store();
//   return <div>{${cleanProps[0] || "value"}}</div>;
// }
`;

  return {
    code,
    library: "zustand",
    dependencies: ["zustand"],
  };
}

/**
 * Generate Jotai atoms.
 */
function generateJotaiAtoms(storeName, props, typescript) {
  const name = capitalize(storeName);
  const cleanProps = props.filter((p) => !p.startsWith("..."));

  const atoms = cleanProps
    .map((p) => {
      const type = inferPropType(p);
      const typeAnnotation = typescript ? `<${type}>` : "";
      const defaultVal = getDefaultValue(type);
      return `export const ${p}Atom = atom${typeAnnotation}(${defaultVal});`;
    })
    .join("\n");

  const derivedAtom = cleanProps.length > 1
    ? `\n// Combined derived atom for convenience\nexport const ${storeName}Atom = atom((get) => ({\n${cleanProps.map((p) => `  ${p}: get(${p}Atom),`).join("\n")}\n}));\n`
    : "";

  const code = `// Auto-generated by react-prop-optimizer — Jotai
import { atom } from "jotai";

${atoms}
${derivedAtom}
// --- Usage Example ---
// import { useAtom } from "jotai";
// import { ${cleanProps[0] || "value"}Atom } from "./${storeName}Atoms";
//
// function MyComponent() {
//   const [${cleanProps[0] || "value"}, set${capitalize(cleanProps[0] || "value")}] = useAtom(${cleanProps[0] || "value"}Atom);
//   return <div>{${cleanProps[0] || "value"}}</div>;
// }
`;

  return {
    code,
    library: "jotai",
    dependencies: ["jotai"],
  };
}

/**
 * Generate stores for all detected drilling patterns in an analysis result.
 */
function generateStoresFromAnalysis(analysisResult, library) {
  const solutions = [];

  for (const comp of analysisResult.components) {
    if (comp.passthroughs.length === 0) continue;

    const drilledProps = [];
    for (const pt of comp.passthroughs) {
      for (const prop of pt.propsPassedThrough) {
        if (prop !== "...spread" && !drilledProps.includes(prop)) {
          drilledProps.push(prop);
        }
      }
    }

    if (drilledProps.length === 0) continue;

    const storeName = comp.name
      .replace(/Component$/, "")
      .replace(/Container$/, "")
      .replace(/^./, (c) => c.toLowerCase());

    const generated = generateStore({
      storeName,
      props: drilledProps,
      library,
      typescript: true,
    });

    solutions.push({
      sourceComponent: comp.name,
      storeName,
      drilledProps,
      generated,
    });
  }

  return solutions;
}

/**
 * Get a default value for a TypeScript type string.
 */
function getDefaultValue(type) {
  switch (type) {
    case "string":
      return '""';
    case "number":
      return "0";
    case "boolean":
      return "false";
    case "React.ReactNode":
      return "null";
    case "React.CSSProperties":
      return "{}";
    default:
      if (type.includes("=>")) return "() => {}";
      return "null as any";
  }
}

module.exports = {
  generateStore,
  generateStoresFromAnalysis,
};
