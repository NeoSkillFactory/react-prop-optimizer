const { capitalize, inferPropType } = require("./utils");

/**
 * Generate a React Context API implementation from analysis results.
 *
 * @param {object} options
 * @param {string} options.contextName - Name for the context (e.g. "Theme", "User")
 * @param {string[]} options.props - Prop names to include in the context
 * @param {boolean} [options.typescript=true] - Generate TypeScript interfaces
 * @returns {{ contextCode: string, providerCode: string, hookCode: string, fullCode: string }}
 */
function generateContext(options) {
  const { contextName, props, typescript = true } = options;
  const ctxName = capitalize(contextName);

  const interfaceBlock = typescript ? generateInterface(ctxName, props) : "";
  const contextBlock = generateContextCreation(ctxName, props, typescript);
  const providerBlock = generateProvider(ctxName, props, typescript);
  const hookBlock = generateHook(ctxName);
  const usageExample = generateUsageExample(ctxName, props);

  const fullCode = [
    '// Auto-generated by react-prop-optimizer',
    'import React, { createContext, useContext, useState, useMemo } from "react";',
    "",
    interfaceBlock,
    contextBlock,
    "",
    providerBlock,
    "",
    hookBlock,
    "",
    `// --- Usage Example ---`,
    `// ${usageExample.split("\n").join("\n// ")}`,
  ]
    .filter(Boolean)
    .join("\n");

  return {
    contextCode: contextBlock,
    providerCode: providerBlock,
    hookCode: hookBlock,
    interfaceBlock,
    fullCode,
  };
}

/**
 * Generate a TypeScript interface for the context value.
 */
function generateInterface(ctxName, props) {
  const fields = props
    .map((p) => {
      if (p.startsWith("...")) return null;
      const type = inferPropType(p);
      return `  ${p}: ${type};`;
    })
    .filter(Boolean)
    .join("\n");

  return `export interface ${ctxName}ContextValue {\n${fields}\n}`;
}

/**
 * Generate the createContext call.
 */
function generateContextCreation(ctxName, props, typescript) {
  const typAnnotation = typescript ? `<${ctxName}ContextValue | undefined>` : "";
  return `const ${ctxName}Context = createContext${typAnnotation}(undefined);`;
}

/**
 * Generate the Provider component.
 */
function generateProvider(ctxName, props, typescript) {
  const cleanProps = props.filter((p) => !p.startsWith("..."));
  const propsInterface = typescript
    ? `interface ${ctxName}ProviderProps {\n  children: React.ReactNode;\n${cleanProps.map((p) => `  ${p}?: ${inferPropType(p)};`).join("\n")}\n}`
    : "";

  const paramType = typescript ? `: ${ctxName}ProviderProps` : "";
  const destructured = ["children", ...cleanProps].join(", ");
  const memoFields = cleanProps.map((p) => `    ${p},`).join("\n");
  const memoDeps = cleanProps.join(", ");

  return `${propsInterface}

export function ${ctxName}Provider({ ${destructured} }${paramType}) {
  const value = useMemo(
    () => ({
${memoFields}
    }),
    [${memoDeps}]
  );

  return (
    <${ctxName}Context.Provider value={value}>
      {children}
    </${ctxName}Context.Provider>
  );
}`;
}

/**
 * Generate the custom hook for consuming the context.
 */
function generateHook(ctxName) {
  return `export function use${ctxName}() {
  const context = useContext(${ctxName}Context);
  if (context === undefined) {
    throw new Error("use${ctxName} must be used within a ${ctxName}Provider");
  }
  return context;
}`;
}

/**
 * Generate a usage example comment.
 */
function generateUsageExample(ctxName, props) {
  const cleanProps = props.filter((p) => !p.startsWith("..."));
  const propsStr = cleanProps.map((p) => `${p}={${p}}`).join(" ");

  return `// In the parent component:
// <${ctxName}Provider ${propsStr}>
//   <ChildComponent />
// </${ctxName}Provider>
//
// In any child component:
// const { ${cleanProps.join(", ")} } = use${ctxName}();`;
}

/**
 * Generate contexts for all detected drilling patterns in an analysis result.
 *
 * @param {object} analysisResult - Result from react-analyzer
 * @returns {object[]} Array of generated context solutions
 */
function generateContextsFromAnalysis(analysisResult) {
  const solutions = [];

  for (const comp of analysisResult.components) {
    if (comp.passthroughs.length === 0) continue;

    // Group drilled props by destination component
    const propGroups = {};
    for (const pt of comp.passthroughs) {
      for (const prop of pt.propsPassedThrough) {
        if (prop === "...spread") continue;
        if (!propGroups[prop]) propGroups[prop] = new Set();
        propGroups[prop].add(pt.childComponent);
      }
    }

    // Collect all drilled prop names
    const drilledProps = Object.keys(propGroups);
    if (drilledProps.length === 0) continue;

    // Generate a context based on the component name
    const contextName = comp.name.replace(/Component$/, "").replace(/Container$/, "");
    const generated = generateContext({
      contextName,
      props: drilledProps,
      typescript: true,
    });

    solutions.push({
      sourceComponent: comp.name,
      contextName,
      drilledProps,
      affectedChildren: [...new Set(Object.values(propGroups).flatMap((s) => [...s]))],
      generated,
    });
  }

  return solutions;
}

module.exports = {
  generateContext,
  generateContextsFromAnalysis,
};
